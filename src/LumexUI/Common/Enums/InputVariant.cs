namespace LumexUI.Common;

/// <summary>
/// Specifies the different variants for an input component.
/// </summary>
public enum InputVariant
{
    /// <summary>
    /// A flat variant input.
    /// </summary>
    Flat,

    /// <summary>
    /// An outlined variant input.
    /// </summary>
    Outlined,

    /// <summary>
    /// An underlined variant input.
    /// </summary>
    Underlined
}