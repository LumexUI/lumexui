namespace LumexUI.Common;

/// <summary>
/// Specifies the sort direction for a <see cref="LumexDataGrid{T}"/> column.
/// </summary>
public enum SortDirection
{
    /// <summary>
    /// Sorts the data in automatic order.
    /// </summary>
    Auto,

    /// <summary>
    /// Sorts the data in ascending order.
    /// </summary>
    Ascending,

    /// <summary>
    /// Sorts the data in descending order.
    /// </summary>
    Descending
}
