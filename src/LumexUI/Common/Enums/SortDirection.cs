namespace LumexUI.Common;

/// <summary>
/// Specifies the sort direction for a column in the <see cref="LumexDataGrid{T}"/>.
/// </summary>
public enum SortDirection
{
	/// <summary>
	/// An automatic sort direction.
	/// </summary>
	Auto,

	/// <summary>
	/// An ascending sort order.
	/// </summary>
	Ascending,

	/// <summary>
	/// A descending sort order.
	/// </summary>
	Descending
}
