namespace LumexUI.Common;

/// <summary>
/// Specifies the placement options for the <see cref="LumexBadge"/>.
/// </summary>
public enum BadgePlacement
{
	/// <summary>
	/// Positions the badge at the top, aligned to the start.
	/// </summary>
	TopStart,

	/// <summary>
	/// Positions the badge at the top, aligned to the end.
	/// </summary>
	TopEnd,

	/// <summary>
	/// Positions the badge at the bottom, aligned to the start.
	/// </summary>
	BottomStart,

	/// <summary>
	/// Positions the badge at the bottom, aligned to the end.
	/// </summary>
	BottomEnd
}
