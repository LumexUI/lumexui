namespace LumexUI.Common;

/// <summary>
/// Specifies the size options for a <see cref="LumexModal"/>.
/// </summary>
public enum ModalSize
{
	/// <summary>
	/// An extra-small size.
	/// </summary>
	XSmall,

	/// <summary>
	/// A small size.
	/// </summary>
	Small,

	/// <summary>
	/// A medium size.
	/// </summary>
	Medium,

	/// <summary>
	/// A large size.
	/// </summary>
	Large,

	/// <summary>
	/// The modal spans the full available space.
	/// </summary>
	Full
}
