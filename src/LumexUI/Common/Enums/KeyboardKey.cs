namespace LumexUI.Common;

/// <summary>
/// Represents common keyboard keys for UI interactions.
/// </summary>
public enum KeyboardKey
{
	/// <summary>
	/// The Command key (⌘), commonly found on Apple keyboards.
	/// </summary>
	Command,

	/// <summary>
	/// The Shift key.
	/// </summary>
	Shift,

	/// <summary>
	/// The Control key (Ctrl).
	/// </summary>
	Control,

	/// <summary>
	/// The Option key (⌥), commonly found on Apple keyboards.
	/// </summary>
	Option,

	/// <summary>
	/// The Alt key (⎇), commonly found on Windows and Linux keyboards.
	/// </summary>
	Alt,

	/// <summary>
	/// The Windows key (⊞).
	/// </summary>
	Win,

	/// <summary>
	/// The Function (fn) key, often found on laptops.
	/// </summary>
	Fn,

	/// <summary>
	/// The Enter key.
	/// </summary>
	Enter,

	/// <summary>
	/// The Delete key.
	/// </summary>
	Delete,

	/// <summary>
	/// The Escape key (Esc).
	/// </summary>
	Escape,

	/// <summary>
	/// The Tab key.
	/// </summary>
	Tab,

	/// <summary>
	/// The Caps Lock key.
	/// </summary>
	CapsLock,

	/// <summary>
	/// The Up Arrow key.
	/// </summary>
	Up,

	/// <summary>
	/// The Right Arrow key.
	/// </summary>
	Right,

	/// <summary>
	/// The Down Arrow key.
	/// </summary>
	Down,

	/// <summary>
	/// The Left Arrow key.
	/// </summary>
	Left,

	/// <summary>
	/// The Page Up key.
	/// </summary>
	PageUp,

	/// <summary>
	/// The Page Down key.
	/// </summary>
	PageDown,

	/// <summary>
	/// The Home key.
	/// </summary>
	Home,

	/// <summary>
	/// The End key.
	/// </summary>
	End,

	/// <summary>
	/// The Help key.
	/// </summary>
	Help,

	/// <summary>
	/// The Space key (Spacebar).
	/// </summary>
	Space,
}
