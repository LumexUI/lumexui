namespace LumexUI.Common;

/// <summary>
/// Specifies the visual variants of the <see cref="LumexTab"/>.
/// </summary>
public enum TabVariant
{
	/// <summary>
	/// A variant with a solid background.
	/// </summary>
	Solid,

	/// <summary>
	/// A variant with an outlined border.
	/// </summary>
	Outlined,

	/// <summary>
	/// A variant with an underline.
	/// </summary>
	Underlined,

	/// <summary>
	/// A variant without any styling.
	/// </summary>
	Light
}
