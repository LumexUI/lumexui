namespace LumexUI.Common;

/// <summary>
/// Specifies theme color options for a component.
/// </summary>
public enum ThemeColor
{
	/// <summary>
	/// No specific theme color.
	/// </summary>
	None,

	/// <summary>
	/// The default theme color.
	/// </summary>
	Default,

	/// <summary>
	/// The primary theme color.
	/// </summary>
	Primary,

	/// <summary>
	/// The secondary theme color.
	/// </summary>
	Secondary,

	/// <summary>
	/// The success theme color.
	/// </summary>
	Success,

	/// <summary>
	/// The warning theme color.
	/// </summary>
	Warning,

	/// <summary>
	/// The danger theme color.
	/// </summary>
	Danger,

	/// <summary>
	/// The info theme color.
	/// </summary>
	Info
}
