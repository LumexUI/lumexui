namespace LumexUI.Common;

/// <summary>
/// Specifies the selection modes for a component.
/// </summary>
public enum SelectionMode
{
	/// <summary>
	/// No selection allowed.
	/// </summary>
	None,

	/// <summary>
	/// Allows selecting a single item.
	/// </summary>
	Single,

	/// <summary>
	/// Allows selecting multiple items.
	/// </summary>
	Multiple
}
